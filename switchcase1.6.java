public class switchcase6 {
    public static void main(String[] args) {
        Character ch = 'c';

        switch (ch) { 
            case 'a':
                System.out.println("You are a.");
                break;
            case 'c':
                System.out.println("You are c.");
                break;
            default:
                System.out.println("Character not matched.");
        }
    }
}
